package com.roundfeather.persistence.utils.datastore.annotation;

/**
 * This enum is used to identify the type of Datastore key
 *
 * @since 1.0
 */
public enum KeyType {

    /**
     * The Datastore key is a String
     *
     * @since 1.0
     */
    STRING,

    /**
     * The Datastore key is a Long
     *
     * @since 1.0
     */
    LONG
}
